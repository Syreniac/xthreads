#define XTHREADS_EUNDEFINED XTHREADS_EBUSY+1
#define XTHREADS_EPERM XTHREADS_EUNDEFINED+1
#define XTHREADS_ENOMEM XTHREADS_EPERM+1
#define XTHREADS_EAGAIN XTHREADS_ENOMEM+1
#define XTHREADS_ETIMEDOUT XTHREADS_EAGAIN+1

#endif /* XTHREADS_ERRORS_H_ */
